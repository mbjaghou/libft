void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*f;

	i = 0;
	f = (unsigned char *)b;
	while (i < len)
	{
		f[i] = c;
		i++;
	}
	b = (void *)f;
	return (b);
}
